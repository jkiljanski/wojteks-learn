package com.farm;

public class Cow {
		private String name;
		private String race;
	public Cow() {
		
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return name;
	}
	
	public void setRace(String race){
		this.race=race;
	}
	public String getRace(){
		return race;
	}
	
	
}
